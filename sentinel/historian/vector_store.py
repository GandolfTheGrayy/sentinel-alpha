"""
Sentinel Historian — Vector Store Setup and Client Wrapper

This module is the foundation of the Historian agent's RAG pipeline. It
initializes a persistent ChromaDB instance on disk, defines the canonical
collections used across Sentinel (market_events and sec_filings), and
exposes a typed VectorStoreClient wrapper that the rest of the Historian
agent (and the Linguist/Judge agents) use to read and write embeddings.

Collection layout:
  - market_events : historical price-move events with metadata (ticker,
                    date, magnitude, trigger_type). Used by the RAG query
                    interface to surface analogous past events when a new
                    sentiment signal arrives.
  - sec_filings   : chunked text from 8-K / 10-Q filings with metadata
                    (ticker, form_type, filed_date, chunk_index). Used by
                    the Regulatory Whispers detector and Linguistic Drift
                    detector to ground their analyses in primary sources.

Embeddings are generated by ChromaDB's default embedding function
(all-MiniLM-L6-v2 via sentence-transformers) so no external embedding
API call is needed — keeping latency low for high-volume ingestion.

Typical usage:
    from sentinel.historian.vector_store import VectorStoreClient
    client = VectorStoreClient()
    client.upsert_market_event(event_id="AAPL_20240115", ...)
    results = client.query_market_events(query_text="earnings beat guidance", n=5)
"""

