COLLECTION_SEC_FILINGS: str = "sec_filings"

DEFAULT_DB_PATH: str = os.path.join(
    os.path.dirname(os.path.abspath(__file__)),
    "..",
    "..",
    "data",
    "chromadb",
)

# How many results to return when no explicit n_results is provided.
