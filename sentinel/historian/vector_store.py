# ---------------------------------------------------------------------------
# Typed result containers
# ---------------------------------------------------------------------------


@dataclass
class MarketEventDocument:
    """A single market-event record returned from a vector similarity query."""

    event_id: str
    ticker: str
    event_date: str          # ISO-8601 date string "YYYY-MM-DD"
    trigger_type: str        # e.g. "earnings_beat", "guidance_cut", "macro_shock"
    price_change_pct: float  # e.g. +7.3 or -12.1
    description: str         # free-text narrative stored as the document body
    distance: float          # cosine distance from query vector (lower = closer)
    extra_metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class SecFilingChunk:
    """A single chunked SEC filing passage returned from a vector similarity query."""

    chunk_id: str
    ticker: str
    form_type: str    # "8-K" or "10-Q"
    filed_date: str   # ISO-8601 date string "YYYY-MM-DD"
    chunk_index: int  # position of this chunk within the full filing
    text: str         # the raw chunk text used as the ChromaDB document
    distance: float   # cosine distance from query vector
    extra_metadata: dict[str, Any] = field(default_factory=dict)


# ---------------------------------------------------------------------------
# Low-level initialiser helpers
# ---------------------------------------------------------------------------


def build_chroma_client(db_path: Optional[str] = None) -> chromadb.ClientAPI:
    """Create (or reopen) a persistent ChromaDB client at *db_path*."""
    resolved_path = db_path or DEFAULT_DB_PATH
    os.makedirs(resolved_path, exist_ok=True)
    logger.info("Initialising ChromaDB persistent client at: %s", resolved_path)
    client = chromadb.PersistentClient(
        path=resolved_path,
        settings=Settings(
            anonymized_telemetry=False,
            allow_reset=True,
        ),
    )
    return client


def ensure_collection(
    client: chromadb.ClientAPI,
    name: str,
    metadata: Optional[dict[str, Any]] = None,
) -> chromadb.Collection:
    """Return a ChromaDB collection, creating it with *metadata* if absent."""
    collection = client.get_or_create_collection(
        name=name,
        metadata=metadata or {},
    )
    logger.debug("Collection ready: '%s' (count=%d)", name, collection.count())
    return collection


# ---------------------------------------------------------------------------
# High-level typed client wrapper
# ---------------------------------------------------------------------------


class VectorStoreClient:
    """
    Thread-safe wrapper around ChromaDB that enforces Sentinel's collection
    schema and returns typed dataclass results instead of raw dicts.

    Parameters
    ----------
    db_path:
        Override the default on-disk path for the ChromaDB directory.
        Useful for tests that want a temporary directory.
    """

    def __init__(self, db_path: Optional[str] = None) -> None:
        """Initialise the ChromaDB client and ensure both collections exist."""
        self._client = build_chroma_client(db_path)
        self._market_events = ensure_collection(
            self._client,
            COLLECTION_MARKET_EVENTS,
            metadata={
                "description": "Historical market price-move events keyed by ticker+date",
                "hnsw:space": "cosine",
            },
        )
        self._sec_filings = ensure_collection(
            self._client,
            COLLECTION_SEC_FILINGS,
            metadata={
                "description": "Chunked SEC 8-K and 10-Q filing text keyed by chunk_id",
                "hnsw:space": "cosine",
            },
        )

